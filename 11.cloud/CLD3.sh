配额管理
●配额基础
• 管理员可以通过配额限制,防止资源的过度使用
• 配额基本项目,限制每个项目可以使用多少资源
• 这些操作上的功能限制,赋予了管理员对每个项目的精准控制

●资源参数
• 安全组规则:指定每个项目可用的规则数
• 核心:指定每个项可用的VCPU核心数
• 固定IP地址:指定每个项目可用的固定IP数
• 浮劢IP地址:指定每个项目可用的浮劢IP数
• 注入文件大小 :指定每个项目内容大小
• 注入文件路径:指定每个项目注入的文件路径长度
• 注入文件:指定每个项目允许注入的文件数目

• 实例:指定每个项目可创建的虚拟机实例数目
• 密钥对:指定每个项可创建的密钥数
• 元数据:指定每个项目可用的元数据数目
• 内存:指定每个项目可用的最大内存
• 安全组:指定每个项目可创建的安全组数目
