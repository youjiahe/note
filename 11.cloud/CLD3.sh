●用户管理
基本概念
• 用户在openstack中用于身份认证
• 管理员用户admin一般在packstack安装过程中创建
• 其他用户由管理员用户创建,并指定可以访问的项目
• 非管理员用户创建后,保存到MariaDB中
• 非管理员用户具有以下权限
  – 启劢实例
  – 创建卷和快照
  – 创建镜像
  – 分配浮劢IP
  – 创建网络和路由器
  – 创建防火墙以及规则、规则策略
  – 查看网络拓扑、项目使用概况等
