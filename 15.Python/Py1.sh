●python起源
Guido van Rossum 作者  1991年

●python版本 
  python2.X 目前所有系统默认支持的版本
  python3.X 
  — 2009年发布
  — 语法有较大的调整
  — python的发展趋势
  — 学习python3.7.1稳定版
  
●为什么要学python
  — 高级：有高级的数据结构，缩短开发时间与代码量，非常的简洁；
  — 面向对象：为数据和逻辑相分离的结构化和过程编程添加了新的活力
  — 可升级：提供了基本的开发模块，可以在他的上面开发软件，实现代码重用
  — 可扩展：通过将其分离为多个文件或模块加以组织管理

