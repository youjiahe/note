设置pycharm  #教学环境默认有
● Pycharm是由JetBrains打造的一款Python IDE
● 支持的功能有:
–  调试、语法高亮
–  Project管理、代码跳转
–  智能提示、自动完成
–  单元测试、版本控制
● 下载地址:https://www.jetbrains.com/pycharm/download

● 使用pycharm
打开pycharm
选择create new project
在左上角找到file-----setting----editor
找到editor——font——size
解释器editor—project——project interpreter
左侧小齿轮Add local
选择虚拟环境 /usr/local/bin/python3.6  添加3.6
